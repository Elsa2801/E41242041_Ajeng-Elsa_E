
package person.java;

public class PersonJava {
    String Name;
    int Age;
    
    public PersonJava(String Name, int Age) {
        this.Name = Name;
        this.Age = Age;
    }
    
    public String getName() {
        return Name;
    }
    
    public int getAge() {
        return Age;
    }
    
    public void setAge(int Age) {
        this.Age = Age;
    }
   
    
}
